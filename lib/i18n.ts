export type Language = keyof typeof translations;

// RTL 语言列表
export const RTL_LANGUAGES: Language[] = ['ar']

// 检查是否为 RTL 语言
export function isRTL(lang: Language): boolean {
  return RTL_LANGUAGES.includes(lang)
}

